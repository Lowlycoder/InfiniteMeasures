    <h2 class="text-blue">Empreinte carbone, bilan carbone et empreinte écologique… Comment s’y retrouver ?</h2>
    <p> L’empreinte écologique (ou empreinte environnementale) est un indicateur de l’effet des activités humaines sur
        la
        nature. Contrairement à l’empreinte carbone, elle n’est pas focalisée sur les GES et considère l’impact
        environnemental plus global en comparant d’une part “la demande” (les activités humaines) et d’autre part
        “l’offre”
        (la capacité de la Terre à produire des ressources, à absorber des déchets et à se régénérer : la
        biocapacité).<br>

        L’empreinte écologique tient compte de :<br>

        L’empreinte carbone (qui représente quand même les ¾ de l’empreinte écologique mondiale)<br>
        Des bâtis (routes, bâtiments), des zones de pêches (sauvages ou élevages, mer ou eau douce) et produits
        forestiers
        (bois de chauffe, pâte à papier et produits dérivés du bois)<br>
        L’impact des pâturages et cultures (nourriture humaine et animale, textiles)<br>
    </p>
